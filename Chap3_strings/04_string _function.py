story = "once upon a time, a boy named JAyesh Lived in a Boarding School, named Smita Patil Public School"
print(story)

# string function

print(len(story)) # length of string

print(story.endswith("School"))  # represents the ending of the line
print(story.endswith("notes"))  # same as above, outputs as true/false

print(story.count("a"))  # help us know the character in the line above
print(story.count("i"))

print(story.capitalize()) # capitalise the first character of the sentence

print(story.find("School"))  # Finds the character position in sentence
print(story.find("h"))

print(story.replace("time" , "era"))   # for replacing the word


