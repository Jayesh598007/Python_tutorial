# Loops in Python
# to repeat some set of statements on our program, we use loops

'''
There are two types of loops in python:
1) while loop
2) for loop
'''
# while loop

i = 0
while i<10:  # until the condition is true(ie;favourable), the loop will executes. it will only stop when condition become false'  
    print("yes" + str(i))
    i = i+1
print("done")



