# range function
# it is used in python to generate a sequence of numbers

for i in range(8):
    print(i)

#^^^^^ output is in range from 0-8 in above program
print("Done")
for i in range(2, 8):  #within "()" is specified as ( start, stop)
    print(i)

# ^^^^ output is in range from 2-8 in above program

print("Done")
for i in range(1, 8, 2):  #within "()" is specified as ( start, stop, step-size)
    print(i)

# ^^^^^ output is in range from 1-8, with the step-size of 2 in above program
