# "if , else and elif" statements are multiway decision taken by the program
#  decisions taken are due to certain conditions in our code


# if-elif-else ladder in python
'''
a = 45

if(a>30):
    print("The value of a is greater than 30")
elif(a>17):
    print("The value of a is greater than 17")
elif(a>12):
    print("The value of a is greater than 12")
elif(a>7):
    print("The value of a is greater than 7")
else:
     print("The value of a is not greater than 3 or 7")
print("Done")
'''

