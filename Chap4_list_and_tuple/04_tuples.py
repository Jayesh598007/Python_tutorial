# creating a tuple using ()
# tuple is a immutable data type
'''
() is a tuple (empty tuple)
[] is a list (empty list)
'''
t = (1, 2, 4, 5) 
t1 = ()   # empty tuple
t2 = (1,)  # tuple with single element
# t1 = (1)  # wrong way to declare a tuple with single element

#printing the elements of tuple
print(t[2])

#cannot update the values of tuple
# t[0] = 34   # throws error in terminal

print(t1)
print(t2)


