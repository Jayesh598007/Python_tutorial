 # list "l1"
l1 = [1, 43, 6, 24, 52, 7, 18]
print(l1)

# sorting the list (ascending)
l1.sort()
print(l1)

# reverse the list (descending)
l1.reverse()
print(l1)

# append the list (adds to the end of the list)
l1.append(45)
l1.append(85)
print(l1)

# insert the list (insert new data at the given postn)
l1.insert(0, 77)  #insert 77 at index 0
l1.insert(2,452)   #insert 452 at index 2
print(l1)

# pop from list (remove a element at index postn)
l1.pop(1)
print(l1)

# remove from list (remove the element by itself)
l1.remove(452)
print(l1)

